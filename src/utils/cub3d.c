#include <cub3d.h>

t_cub3d	*cub3d(void)
{
	static t_cub3d	singleton;

	return (&singleton);
}

void	cub3d_exit(void)
{
	if (cub3d()->window.initialized)
		destroy_window(&cub3d()->window);
	exit(cub3d()->error);
}
