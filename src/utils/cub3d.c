#include <cub3d.h>

t_cub3d	*cub3d(void)
{
	static t_cub3d	singleton;

	return (&singleton);
}

void	cub3d_exit(int code)
{
	free_map(cub3d()->curr_map);
	clear_game(&cub3d()->game);
	clear_sprite(&cub3d()->placeholder);
	ftm_clear_window(&cub3d()->window);
	exit(code);
}
